    private void uploadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_uploadMouseClicked
         JFileChooser openFileChooser = new JFileChooser();
        int result = openFileChooser.showOpenDialog(null);
        if(result == JFileChooser.APPROVE_OPTION){
            musica = openFileChooser.getSelectedFile();
        }
        String currentDirectory = System.getProperty("user.dir");
        currentDirectory += "/MÃºsica";
        String nomeMusica= musica.getName();
        File music = new File( currentDirectory, nomeMusica);

        try{
            music.createNewFile();
            Files.copy(musica.toPath(), music.toPath(), StandardCopyOption.REPLACE_EXISTING);
        }
        catch(Exception e){}
    }//GEN-LAST:event_uploadMouseClicked

